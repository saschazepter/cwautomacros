<h2>CWautomacros: a collection of autoconf m4 macros by Carlo Wood</h2>

<p>This project is intended for <em>developers</em> (people who use CVS to download a project) of
projects that use <i>cwautomacros</i>.</p>

<p>If you have downloaded a project that sent you here, then
get the sources with <a href="http://developer.berlios.de/cvs/?group_id=6077">Anonymous CVS Access</a>,
or download the latest <a href="http://developer.berlios.de/project/showfiles.php?group_id=6077">tar ball</a>.</p>

<p>It's rather plug-and-play: Everything is installed in <code>/usr/share/cwautomacros</code>
(or in <code>$CWAUTOMACROSPREFIX/share/cwautomacros</code> if you set that environment variable).
Then simply go back to the project that sent you hear and run it's 'autogen.sh'.</p>

